public class Ship {
    public string sprite;
    public int price;
    public float speed;
    public int hp;
    public float damage;

    public Ship(string sprite, int price, float speed, int hp, float damage) {
        this.sprite = sprite;
        this.price = price;
        this.speed = speed;
        this.hp = hp;
        this.damage = damage;
    }
}
